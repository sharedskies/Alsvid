function include_kaplan_delay(jd_tts)

  jd_tdbs = zeros(BigFloat,length(jd_tts))
  for i in 1:length(jd_tts)
           
    t = (jd_tts[i] - BigFloat(2451545.0))/BigFloat(36525.0)

    dt = BigFloat(0.001657)*sin(BigFloat(628.3076)*t + BigFloat(6.2401))
