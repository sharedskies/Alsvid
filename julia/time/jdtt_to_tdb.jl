#!/usr/local/bin/julia

#  jdtt_to_tdb
#
#    Return JD_TDB from JD_TT in TAI units

#    Transform the arrival time of a photon measured by an observer's TAI clock
#      on Earth's surface to represent a clock at the geocenter affected by
#      the presence of the Sun and other bodies in the solar system. This
#      is termed the Einstein delay at the geocenter.
#
#    Papers by Eastman, Markwardt, and Fairhead describe a periodic variation
#      with an amplitude of 3.4 milliseconds and dominant period of 1 year.
#      TDB converges on the JPL ephemeris time TEPH, the coordinate time CT
#      in the JPL ephemerides (Kaplan, 2005; Eastman, et al, 2010).
#
#    ERFA code derived from SOFA is a source for the Fairhead parameters
#      which in full form were not published in the 1990 paper.  Here we
#      also include a much faster and less accurate version from Kaplan
#      which is sufficiently precise for photometry in the regime
#      of microseconds, while the Fairhead model is accurate to nanoseconds.

#    The correction from UTC to TDB is 
#
#      Delta_Clock = N_Leap + 32.184 + (TDB - TT) seconds
#
#    This routine provides the third term of that expression.

#    Uses BigFloat data type for unlimited precision

#    Explanations: 
#      George H. Kaplan, USNO Circular 179, 2005
#      C. Markwardt, online 2001, retrieved December, 2021
#      L. Fairhead, P. Bretagnon, A&A 229, 240 (1990)
#      J. Eastman, R. Siverd, B.Scott Gaudi, PASP 122, 935 (2010)

#    Credit and comment:
#      The expansion constants in this  code come directly from
#      tdb2tdt written by Craig B. Markwardt and incorporated into 
#      Exofastv2 by Jason Eastman. The same parameters appear in 
#      the ERFA code as of 2022-01-09.  These 780 coefficients are said
#      in the Fairhead paper to provide an accuracy at the nanosecond
#      level, while the paper provides a subset with microsecond accuracy.

#    Validity: 
#      Expected to be valid for a few thousand years around J2000.0 to
#      accuracy of ~1 ns if using the Fairhead model, ~1 microsec if using
#      Kaplan model.

#    Explicit input:
#      JD_TT

#    Explicit output:
#      JD_TDB

#    Required data:
#      None

#    John Kielkopf (kielkopf@louisville.edu)
#    MIT License
#    Copyright 2022
#    
#    2022-01-22 Version 1.0



using Printf

#  Use true to select lower precision with faster execution 

kaplan_flag = true

#  Read and parse the command line arguments 
#  Return BigFloat array

function get_jdtt_args(args)
  nargs = length(args)
  
  if nargs < 1
    print("Enter space delimited JD_TTs on the command line \n")
    exit()
  end
 
  jd_tts = zeros(BigFloat,nargs)
  for i in 1:nargs
    jd_tts[i] = parse(BigFloat,args[i])
  end

  return jd_tts
end




